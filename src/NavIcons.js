// Функция получения цвета из CSS переменной
const getColorFromTheme = (active, type = 'button') => {
  if (typeof window === 'undefined') return '#858589';
  
  const root = document.documentElement;
  let color;
  
  if (active) {
    color = getComputedStyle(root).getPropertyValue('--tg-theme-button-color').trim();
  } else {
    color = getComputedStyle(root).getPropertyValue('--tg-theme-hint-color').trim();
  }
  
  // Fallback если переменная не установлена
  return color || (active ? '#3390ec' : '#858589');
};

